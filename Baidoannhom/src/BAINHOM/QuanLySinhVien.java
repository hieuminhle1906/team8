public boolean ghifile(ArrayList<SinhVien> listsv, String duongdan){
    try{
        FileOutputStream fos = new FileOutputStream(duongdan);
        OutputStreamWriter osw= new OutputStreamWriter(fos,"UTF-8");
        BufferedWriter bw = new BufferedWriter(osw);
        for(SinhVien sv: listsv){
            String luudong= sv.getMasv()+"_" + sv.getHoten()+"_"+sv.getNamsinh()+"_"+sv.getNganh()+"_"+sv.getEmail()+"_"+sv.getGPA();
            bw.write(luudong);
            bw.newLine();
        }
        bw.close();
        osw.close();
        fos.close();
        return true;
    }
    catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi hệ thống !!!"); 
    }
   return false; 
}
