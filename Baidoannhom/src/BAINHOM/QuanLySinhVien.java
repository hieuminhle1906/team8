public void docfile(String duongdan){
    ArraySV.clear();
    try{
        FileInputStream fin = new FileInputStream(duongdan);
        InputStreamReader in = new InputStreamReader(fin, "UTF-8");
        BufferedReader bfr = new BufferedReader(in);
        String docdong=bfr.readLine();
        while(docdong!=null){
            SinhVien listdoc= new SinhVien();
            String arr[]=docdong.split(";");
            if(arr.length==7)
            listdoc.setMasv(arr[0]);
            listdoc.setHoten(arr[1]);
            listdoc.setNamsinh(Integer.parseInt(arr[2]));
            listdoc.setEmail(arr[4]);
            listdoc.setNganh(arr[5]);
            listdoc.setGPA(Double.parseDouble(arr[6]));
            ArraySV.add(listdoc);
        }
        docdong=bfr.readLine();
    }
    catch(Exception ex){
        JOptionPane.showMessageDialog(this, "Loi he thong ");
    }
}
